create or replace function public.cast_vote(p_vote_id uuid, p_choice text)
returns table (
  vote_id uuid,
  choice text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_vote public.votes%rowtype;
  v_membership public.committee_memberships%rowtype;
begin
  select *
  into v_vote
  from public.votes
  where id = p_vote_id
    and status = 'open';

  if not found then
    raise exception 'Vote is closed or unavailable.';
  end if;

  select *
  into v_membership
  from public.committee_memberships
  where committee_id = v_vote.committee_id
    and user_id = auth.uid()
    and role = 'delegate'
    and is_active;

  if not found then
    raise exception 'Delegate access required.';
  end if;

  insert into public.vote_ballots (
    vote_id,
    committee_id,
    membership_id,
    choice
  )
  values (
    v_vote.id,
    v_vote.committee_id,
    v_membership.id,
    p_choice
  )
  on conflict (vote_id, membership_id)
  do update
  set choice = excluded.choice,
      cast_at = now();

  perform public.append_event_log(
    v_vote.committee_id,
    'delegate',
    v_membership.country,
    'vote_cast',
    format('%s updated a ballot.', v_membership.country),
    jsonb_build_object('choice', p_choice)
  );

  return query
  select v_vote.id, p_choice;
end;
$$;

create or replace function public.close_vote(p_code text, p_vote_id uuid)
returns table (
  vote_id uuid,
  yes_count bigint,
  no_count bigint,
  abstain_count bigint
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_committee public.committees%rowtype;
  v_vote public.votes%rowtype;
  v_yes bigint;
  v_no bigint;
  v_abstain bigint;
begin
  select c.*
  into v_committee
  from public.committees c
  where c.code = upper(trim(p_code));

  if not found or not public.is_committee_chair(v_committee.id) then
    raise exception 'Chair access required.';
  end if;

  select *
  into v_vote
  from public.votes
  where id = p_vote_id
    and committee_id = v_committee.id
    and status = 'open';

  if not found then
    raise exception 'Open vote not found.';
  end if;

  select
    count(*) filter (where choice = 'yes'),
    count(*) filter (where choice = 'no'),
    count(*) filter (where choice = 'abstain')
  into v_yes, v_no, v_abstain
  from public.vote_ballots
  where vote_id = v_vote.id;

  update public.votes
  set status = 'closed',
      closed_at = now()
  where id = v_vote.id;

  update public.committees
  set phase = 'formal_debate'
  where id = v_committee.id;

  perform public.append_event_log(
    v_committee.id,
    'chair',
    'Chair',
    'vote_closed',
    format(
      'Closed vote on "%s" (Yes %s, No %s, Abstain %s).',
      v_vote.title,
      v_yes,
      v_no,
      v_abstain
    ),
    jsonb_build_object(
      'yes', v_yes,
      'no', v_no,
      'abstain', v_abstain
    )
  );

  return query
  select v_vote.id, v_yes, v_no, v_abstain;
end;
$$;


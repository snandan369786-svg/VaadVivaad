create or replace function public.generate_committee_code()
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  candidate text;
begin
  loop
    candidate := 'MUN-' || upper(substr(encode(gen_random_bytes(3), 'hex'), 1, 4));
    exit when not exists (
      select 1 from public.committees where code = candidate
    );
  end loop;

  return candidate;
end;
$$;

create or replace function public.lookup_committee(p_code text)
returns table (
  committee_id uuid,
  code text,
  name text,
  topic text,
  phase text,
  status_note text,
  delegate_count bigint,
  claimed_countries text[]
)
language sql
stable
security definer
set search_path = public
as $$
  select
    c.id as committee_id,
    c.code,
    c.name,
    c.topic,
    c.phase,
    c.status_note,
    count(m.id) filter (where m.role = 'delegate' and m.is_active) as delegate_count,
    coalesce(
      array_agg(m.country order by m.country)
        filter (where m.role = 'delegate' and m.is_active and m.country is not null),
      '{}'::text[]
    ) as claimed_countries
  from public.committees c
  left join public.committee_memberships m
    on m.committee_id = c.id
  where c.code = upper(trim(p_code))
  group by c.id, c.code, c.name, c.topic, c.phase, c.status_note;
$$;


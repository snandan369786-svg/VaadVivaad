create extension if not exists pgcrypto;

create table if not exists public.committees (
  id uuid primary key default gen_random_uuid(),
  code text not null unique,
  chair_token text not null unique,
  name text not null,
  topic text not null,
  phase text not null default 'formal_debate',
  status_note text,
  current_speaker_country text,
  created_by uuid not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.committee_memberships (
  id uuid primary key default gen_random_uuid(),
  committee_id uuid not null references public.committees(id) on delete cascade,
  user_id uuid not null,
  role text not null check (role in ('chair', 'delegate')),
  country text,
  display_name text,
  is_active boolean not null default true,
  joined_at timestamptz not null default now(),
  unique (committee_id, user_id)
);

create unique index if not exists committee_delegate_country_unique
on public.committee_memberships (committee_id, lower(country))
where role = 'delegate' and is_active and country is not null;

create table if not exists public.speaker_requests (
  id uuid primary key default gen_random_uuid(),
  committee_id uuid not null references public.committees(id) on delete cascade,
  membership_id uuid not null references public.committee_memberships(id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'accepted', 'dismissed', 'completed')),
  requested_at timestamptz not null default now(),
  reviewed_at timestamptz,
  completed_at timestamptz
);

create unique index if not exists speaker_request_pending_unique
on public.speaker_requests (committee_id, membership_id)
where status = 'pending';

create table if not exists public.speaker_queue (
  id uuid primary key default gen_random_uuid(),
  committee_id uuid not null references public.committees(id) on delete cascade,
  membership_id uuid not null references public.committee_memberships(id) on delete cascade,
  country text not null,
  order_index integer not null,
  status text not null default 'queued' check (status in ('queued', 'current', 'completed')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists speaker_queue_order_unique
on public.speaker_queue (committee_id, order_index);

create unique index if not exists speaker_queue_active_membership_unique
on public.speaker_queue (committee_id, membership_id)
where status in ('queued', 'current');

create table if not exists public.votes (
  id uuid primary key default gen_random_uuid(),
  committee_id uuid not null references public.committees(id) on delete cascade,
  title text not null,
  description text,
  motion_type text not null default 'substantive',
  threshold text not null default 'simple_majority',
  status text not null default 'open' check (status in ('open', 'closed')),
  opened_at timestamptz not null default now(),
  closed_at timestamptz,
  opened_by uuid not null
);

create table if not exists public.vote_ballots (
  id uuid primary key default gen_random_uuid(),
  vote_id uuid not null references public.votes(id) on delete cascade,
  committee_id uuid not null references public.committees(id) on delete cascade,
  membership_id uuid not null references public.committee_memberships(id) on delete cascade,
  choice text not null check (choice in ('yes', 'no', 'abstain')),
  cast_at timestamptz not null default now(),
  unique (vote_id, membership_id)
);

create table if not exists public.drafts (
  id uuid primary key default gen_random_uuid(),
  committee_id uuid not null references public.committees(id) on delete cascade,
  title text not null,
  description text,
  status text not null default 'open' check (status in ('open', 'closed')),
  created_at timestamptz not null default now(),
  closed_at timestamptz
);

create table if not exists public.draft_signatories (
  id uuid primary key default gen_random_uuid(),
  draft_id uuid not null references public.drafts(id) on delete cascade,
  committee_id uuid not null references public.committees(id) on delete cascade,
  membership_id uuid not null references public.committee_memberships(id) on delete cascade,
  signed_at timestamptz not null default now(),
  unique (draft_id, membership_id)
);

create table if not exists public.event_log (
  id bigint generated by default as identity primary key,
  committee_id uuid not null references public.committees(id) on delete cascade,
  actor_role text not null,
  actor_label text not null,
  kind text not null,
  summary text not null,
  detail jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;


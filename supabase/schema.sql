create or replace function public.toggle_draft_signatory(p_draft_id uuid)
returns table (
  draft_id uuid,
  signed boolean
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_draft public.drafts%rowtype;
  v_membership public.committee_memberships%rowtype;
  v_existing public.draft_signatories%rowtype;
begin
  select *
  into v_draft
  from public.drafts
  where id = p_draft_id
    and status = 'open';

  if not found then
    raise exception 'Draft is closed or unavailable.';
  end if;

  select *
  into v_membership
  from public.committee_memberships
  where committee_id = v_draft.committee_id
    and user_id = auth.uid()
    and role = 'delegate'
    and is_active;

  if not found then
    raise exception 'Delegate access required.';
  end if;

  select *
  into v_existing
  from public.draft_signatories
  where draft_id = v_draft.id
    and membership_id = v_membership.id;

  if found then
    delete from public.draft_signatories
    where id = v_existing.id;

    perform public.append_event_log(
      v_draft.committee_id,
      'delegate',
      v_membership.country,
      'draft_signatory_removed',
      format('%s removed support from "%s".', v_membership.country, v_draft.title),
      '{}'::jsonb
    );

    return query
    select v_draft.id, false;
    return;
  end if;

  insert into public.draft_signatories (
    draft_id,
    committee_id,
    membership_id
  )
  values (
    v_draft.id,
    v_draft.committee_id,
    v_membership.id
  );

  perform public.append_event_log(
    v_draft.committee_id,
    'delegate',
    v_membership.country,
    'draft_signatory_added',
    format('%s signed on to "%s".', v_membership.country, v_draft.title),
    '{}'::jsonb
  );

  return query
  select v_draft.id, true;
end;
$$;


create or replace function public.open_vote(
  p_code text,
  p_title text,
  p_description text default null,
  p_motion_type text default 'substantive',
  p_threshold text default 'simple_majority'
)
returns table (
  vote_id uuid
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_committee public.committees%rowtype;
  v_vote_id uuid;
begin
  select c.*
  into v_committee
  from public.committees c
  where c.code = upper(trim(p_code));

  if not found or not public.is_committee_chair(v_committee.id) then
    raise exception 'Chair access required.';
  end if;

  if nullif(trim(p_title), '') is null then
    raise exception 'Vote title is required.';
  end if;

  if exists (
    select 1
    from public.votes
    where committee_id = v_committee.id
      and status = 'open'
  ) then
    raise exception 'Close the current vote before opening another one.';
  end if;

  insert into public.votes (
    committee_id,
    title,
    description,
    motion_type,
    threshold,
    status,
    opened_by
  )
  values (
    v_committee.id,
    trim(p_title),
    nullif(trim(p_description), ''),
    p_motion_type,
    p_threshold,
    'open',
    auth.uid()
  )
  returning id into v_vote_id;

  update public.committees
  set phase = 'voting'
  where id = v_committee.id;

  perform public.append_event_log(
    v_committee.id,
    'chair',
    'Chair',

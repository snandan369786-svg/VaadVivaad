create or replace function public.advance_speaker(p_code text)
returns table (
  queue_id uuid,
  current_speaker_country text,
  queue_status text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_committee public.committees%rowtype;
  v_current public.speaker_queue%rowtype;
  v_next public.speaker_queue%rowtype;
begin
  select c.*
  into v_committee
  from public.committees c
  where c.code = upper(trim(p_code));

  if not found or not public.is_committee_chair(v_committee.id) then
    raise exception 'Chair access required.';
  end if;

  select *
  into v_current
  from public.speaker_queue
  where committee_id = v_committee.id
    and status = 'current'
  order by order_index
  limit 1;

  if found then
    update public.speaker_queue
    set status = 'completed'
    where id = v_current.id;

    update public.speaker_requests
    set status = 'completed',
        completed_at = now()
    where id = (
      select sr.id
      from public.speaker_requests sr
      where sr.committee_id = v_committee.id
        and sr.membership_id = v_current.membership_id
        and sr.status = 'accepted'
      order by sr.requested_at
      limit 1
    );
  end if;

  select *
  into v_next
  from public.speaker_queue
  where committee_id = v_committee.id
    and status = 'queued'
  order by order_index
  limit 1;

  if found then
    update public.speaker_queue
    set status = 'current'
    where id = v_next.id;

    update public.committees
    set current_speaker_country = v_next.country
    where id = v_committee.id;

    perform public.append_event_log(
      v_committee.id,
      'chair',
      'Chair',
      'speaker_advanced',
      case
        when v_current.id is null then format('%s took the floor.', v_next.country)
        else format('Advanced from %s to %s.', v_current.country, v_next.country)
      end,
      jsonb_build_object('current_speaker', v_next.country)
    );

    return query
    select v_next.id, v_next.country, 'current';
  end if;

  update public.committees
  set current_speaker_country = null
  where id = v_committee.id;

  perform public.append_event_log(
    v_committee.id,
    'chair',
    'Chair',
    'speaker_advanced',
    case
      when v_current.id is null then 'Advance requested, but the queue is empty.'
      else format('%s finished and the floor is now empty.', v_current.country)
    end,
    '{}'::jsonb
  );

  return query
  select null::uuid, null::text, 'idle'::text;
end;
$$;


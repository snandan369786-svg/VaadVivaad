create or replace function public.create_committee(p_name text, p_topic text)
returns table (
  committee_id uuid,
  code text,
  chair_token text
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_committee_id uuid;
  v_code text;
  v_chair_token text;
begin
  if v_user_id is null then
    raise exception 'Anonymous session required before creating a committee.';
  end if;

  if nullif(trim(p_name), '') is null or nullif(trim(p_topic), '') is null then
    raise exception 'Committee name and topic are required.';
  end if;

  v_code := public.generate_committee_code();
  v_chair_token := encode(gen_random_bytes(16), 'hex');

  insert into public.committees (
    code,
    chair_token,
    name,
    topic,
    created_by
  )
  values (
    v_code,
    v_chair_token,
    trim(p_name),
    trim(p_topic),
    v_user_id
  )
  returning id into v_committee_id;

  insert into public.committee_memberships (
    committee_id,
    user_id,
    role,
    display_name
  )
  values (
    v_committee_id,
    v_user_id,
    'chair',
    'Chair'
  );

  perform public.append_event_log(
    v_committee_id,
    'chair',
    'Chair',
    'committee_created',
    format('Committee "%s" created.', trim(p_name)),
    jsonb_build_object('topic', trim(p_topic))
  );

  return query
  select v_committee_id, v_code, v_chair_token;
end;
$$;

create or replace function public.claim_chair_access(p_code text, p_chair_token text)
returns table (
  committee_id uuid
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_committee public.committees%rowtype;
  v_existing public.committee_memberships%rowtype;
begin
  if v_user_id is null then
    raise exception 'Anonymous session required before chair access can be claimed.';
  end if;

  select *
  into v_committee
  from public.committees
  where code = upper(trim(p_code))
    and chair_token = p_chair_token;

  if not found then
    raise exception 'Invalid chair link.';
  end if;

  select *
  into v_existing
  from public.committee_memberships
  where committee_id = v_committee.id
    and user_id = v_user_id
    and is_active;

  if found then
    if v_existing.role = 'delegate' then
      raise exception 'This browser is already joined as a delegate for this committee.';
    end if;
  end if;

  if not found then
    insert into public.committee_memberships (
      committee_id,
      user_id,
      role,
      display_name
    )
    values (
      v_committee.id,
      v_user_id,
      'chair',
      'Chair'
    );

    perform public.append_event_log(
      v_committee.id,
      'chair',
      'Chair',
      'chair_rejoined',
      'Chair accessed the control room.',
      '{}'::jsonb
    );
  end if;

  return query
  select v_committee.id;
end;
$$;

create or replace function public.claim_delegate_slot(
  p_code text,
  p_country text,
  p_display_name text default null
)
returns table (
  membership_id uuid,
  committee_id uuid
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_committee public.committees%rowtype;
  v_existing public.committee_memberships%rowtype;
  v_membership_id uuid;
begin
  if v_user_id is null then
    raise exception 'Anonymous session required before joining a committee.';
  end if;

  if nullif(trim(p_country), '') is null then
    raise exception 'Choose a country before joining.';
  end if;

  select *
  into v_committee
  from public.committees
  where code = upper(trim(p_code));

  if not found then
    raise exception 'Committee not found.';
  end if;

  select *
  into v_existing
  from public.committee_memberships
  where committee_id = v_committee.id
    and user_id = v_user_id
    and is_active;

  if found then
    if v_existing.role = 'chair' then
      raise exception 'This browser is already the chair for this committee.';
    end if;

    if v_existing.role = 'delegate' then
      return query
      select v_existing.id, v_existing.committee_id;
      return;
    end if;
  end if;

  if exists (
    select 1
    from public.committee_memberships
    where committee_id = v_committee.id
      and role = 'delegate'
      and is_active
      and lower(country) = lower(trim(p_country))
  ) then
    raise exception 'That country has already been claimed.';
  end if;

  insert into public.committee_memberships (
    committee_id,
    user_id,
    role,
    country,
    display_name
  )
  values (
    v_committee.id,
    v_user_id,
    'delegate',
    trim(p_country),
    nullif(trim(p_display_name), '')
  )
  returning id into v_membership_id;

  perform public.append_event_log(
    v_committee.id,
    'delegate',
    trim(p_country),
    'delegate_joined',
    format('%s joined the committee.', trim(p_country)),
    jsonb_build_object('display_name', nullif(trim(p_display_name), ''))
  );

  return query
  select v_membership_id, v_committee.id;
end;
$$;

create or replace function public.raise_placard(p_code text)
returns table (
  request_id uuid
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_membership public.committee_memberships%rowtype;
  v_request_id uuid;
begin
  select m.*
  into v_membership
  from public.committee_memberships m
  join public.committees c
    on c.id = m.committee_id
  where c.code = upper(trim(p_code))
    and m.user_id = auth.uid()
    and m.role = 'delegate'
    and m.is_active;

  if not found then
    raise exception 'Join the committee as a delegate before raising placard.';
  end if;

  if exists (
    select 1
    from public.speaker_requests
    where committee_id = v_membership.committee_id
      and membership_id = v_membership.id
      and status = 'pending'
  ) then
    raise exception 'You already have a pending placard request.';
  end if;

  if exists (
    select 1
    from public.speaker_queue
    where committee_id = v_membership.committee_id
      and membership_id = v_membership.id
      and status in ('queued', 'current')
  ) then
    raise exception 'You are already on the speakers list.';
  end if;

  insert into public.speaker_requests (
    committee_id,
    membership_id
  )
  values (
    v_membership.committee_id,
    v_membership.id
  )
  returning id into v_request_id;

  perform public.append_event_log(
    v_membership.committee_id,
    'delegate',
    v_membership.country,
    'placard_raised',
    format('%s raised placard.', v_membership.country),
    '{}'::jsonb
  );

  return query
  select v_request_id;
end;
$$;

create or replace function public.queue_speaker_request(p_code text, p_request_id uuid)
returns table (
  queue_id uuid
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_committee public.committees%rowtype;
  v_request public.speaker_requests%rowtype;
  v_membership public.committee_memberships%rowtype;
  v_next_order integer;
  v_queue_id uuid;
begin
  select c.*
  into v_committee
  from public.committees c
  where c.code = upper(trim(p_code));

  if not found or not public.is_committee_chair(v_committee.id) then
    raise exception 'Chair access required.';
  end if;

  select *
  into v_request
  from public.speaker_requests
  where id = p_request_id
    and committee_id = v_committee.id
    and status = 'pending';

  if not found then
    raise exception 'Pending request not found.';
  end if;

  select *
  into v_membership
  from public.committee_memberships
  where id = v_request.membership_id;

  if exists (
    select 1
    from public.speaker_queue
    where committee_id = v_committee.id
      and membership_id = v_membership.id
      and status in ('queued', 'current')
  ) then
    raise exception 'Delegate is already on the speakers list.';
  end if;

  select coalesce(max(order_index), 0) + 1
  into v_next_order
  from public.speaker_queue
  where committee_id = v_committee.id;

  update public.speaker_requests
  set status = 'accepted',
      reviewed_at = now()
  where id = v_request.id;

  insert into public.speaker_queue (
    committee_id,
    membership_id,
    country,
    order_index,
    status
  )
  values (
    v_committee.id,
    v_membership.id,
    v_membership.country,
    v_next_order,
    'queued'
  )
  returning id into v_queue_id;

  perform public.append_event_log(
    v_committee.id,
    'chair',
    'Chair',
    'speaker_queued',
    format('%s added to the speakers list.', v_membership.country),
    jsonb_build_object('order', v_next_order)
  );

  return query
  select v_queue_id;
end;
$$;

